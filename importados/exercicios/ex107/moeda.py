def metade(valor):
    res = valor / 2
    return res


def dobro(valor):
    res = valor * 2
    return res


def aumentar(valor, porcentagem):
     res = valor + (valor * porcentagem / 100)
     return res


def diminuir(valor, porcentagem):
     res = valor - (valor * porcentagem / 100)
     return res
